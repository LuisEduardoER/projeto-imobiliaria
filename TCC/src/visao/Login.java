    private void mostraOcultaConfigurações() {
        if (ADVANCED_PANEL) {
            jbAdvanced.setVisible(false);
            jbCon.setVisible(false);
            jpAdvanced.setVisible(false);
            jbAdvancedPanel.setText("<");
            ADVANCED_PANEL = false;
        } else {
            jbAdvanced.setVisible(true);
            jbCon.setVisible(true);
            jpAdvanced.setVisible(true);
            jbAdvancedPanel.setText(">");
            ADVANCED_PANEL = true;
        }
    }

    private void login() {
        String avisos = "";

        if (jtfUser.getText().equals("")) {
            avisos = avisos + "\n Login não pode ser vazio ";
        }
        if (jpfPass.getPassword().toString().equals("")) {
            avisos = avisos + "\n Senha não pode ser vazio ";
        }

        if (avisos.equals("")) {
            usuario = new Usuario();

            usuario.setUsuarioName(jtfUser.getText());
            usuario.setUsuarioSenha(new String(jpfPass.getPassword()));

            usuarioController = new UsuarioController();
            loginController = new LoginController();

            usuario = usuarioController.buscarUsuarioByLogin(usuario);

            if (loginController.login(usuario)) {
                Session.setUsuario(usuario);
                
                vendaAberturaFechamentoCaixaController = new VendaAberturaFechamentoCaixaController();
                Integer caixasAbertos = vendaAberturaFechamentoCaixaController.rowCountCaixasAbertosByUsuario(usuario.getUsuarioId());
                if(caixasAbertos > 0 ){
                    AberturaCaixa aberturaCaixa = vendaAberturaFechamentoCaixaController.getCaixaAbertoByFuncionario(usuario.getUsuarioId());
                    Session.setCaixa(aberturaCaixa);
                }
                                
                TCC sistema = new TCC();
                this.setVisible(false);
                sistema.setVisible(true);
            }
            jtfUser.requestFocus();
        } else {
            m = new Mensagens();
            m.jopAlerta("Verifique: " + avisos + "!");
            jtfUser.requestFocus();
        }
    }

    private void setFocus(){
        jtfUser.requestFocus();
    }
