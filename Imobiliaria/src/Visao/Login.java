    private void jbLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbLoginActionPerformed
        GerenciarLogin gL = new GerenciarLogin();
        Usuario u = new Usuario();
        u.setNomeUsuario(jtfUsuario.getText());
        u.setSenha(new String(jpfSenha.getPassword()));
        if (gL.login(u) != null) {

            Sessao sessao = Sessao.getInstance();
            sessao.setUsuario(u);
            Principal p = new Principal();
            p.setVisible(true);
            this.dispose();
        } else {
            int x = JOptionPane.showConfirmDialog(this, "Usuário não cadastrado!", "Atenção", JOptionPane.OK_OPTION, JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_jbLoginActionPerformed

